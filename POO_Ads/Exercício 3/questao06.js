// Exemplo de template string com variáveis interpoladas

const nome = "Ely";
const tempoPagamento = 120.56;
const linguagem = "TypeScript";

// Usando template strings para interpolação
const message = `${nome} My payment time is ${tempoPagamento} and my preferred language is ${linguagem}`;

console.log(message);
// Saída: Ely My payment time is 120.56 and my preferred language is TypeScript
